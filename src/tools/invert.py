# Always six chars.
def printable_char(ch):
    if ch >= 32 and ch < 128:
        return "'%c',  " % ch
    else:
        return "0x%02X, " % ch

inverted_data = [((~b & 0x3F) | (b & 0xC0)) & 0xFF for b in DATA]

for line in itertools.batched(inverted_data, 64):
    for span in itertools.batched(line, 8):
        print("    " + "".join(printable_char(x) for x in span))
    print()
